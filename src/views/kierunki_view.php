                    <h1>Gruzja</h1>
                    <p>Otwartość i gościnność to synonimy Gruzji. Ten wschodnioeuropejski kraj to prawdziwy raj dla autostopowiczów. Kierowcy
                        zatrzymują się często, więc nie czeka się długo na podwózkę. Podróż opóźnia czasem kiepski stan dróg, ale w ostatnich
                        latach sytuacja się poprawiła. W Gruzji przymyka się nawet oko na łapanie stopa na autostradach, więc darmowy transport
                        jest na wyciągnięcie ręki. W ekstremalnym przypadku o podwiezienie można poprosić policjantów. Należy pamiętać, że latem
                        w Gruzji jest upalnie (do 40 stopni Celsjusza) natomiast w zimie, szczególnie na północy, potrafi być mroźnie i
                        śnieżnie. Gościnność Gruzinów jest słynna na całym świecie, jednak bardzo łatwo ich urazić. Lepiej nie odmawiać
                        proponowanego alkoholu ani jedzenia. Niektórzy konserwatywni mieszkańcy kraju mogą też negatywnie reagować na zbyt
                        ostentacyjne okazywanie uczuć swojej drugiej połowie. Młodzi Gruzini, szczególnie z obszarów miejskich znają język
                        angielski. Starsze osoby i mieszkańcy wsi porozumiewają się po rosyjsku lub wyłącznie po gruzińsku.</p>
