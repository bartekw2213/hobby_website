                    <h1>Izrael</h1>
                    <p>W Izraelu jazda autostopem jest bardzo popularna. Okazję do darmowej przejażdżki można złapać przy wszystkich drogach,
                        oczywiście poza autostradami. Wyjątkiem jest Zachodni Brzeg, gdzie o podwózkę może być ciężko. Na wielu skrzyżowaniach
                        poza miastami znajdują się tak zwane trempiyady, czyli miejsca, w których gromadzą się autostopowicze. Zazwyczaj są to
                        po prostu przystanki dla autokarów. Warto pamiętać, że w Izraelu temperatury potrafią być naprawdę wysokie (szczególnie
                        od marca do września), więc koniecznie trzeba wyposażyć się w płyny, okrycie głowy, okulary przeciwsłoneczne i krem do
                        opalania. Z większością mieszkańców można dogadać się po angielsku, ale ortodoksyjni Żydzi wolą posługiwać się językiem
                        jidysz, więc podstawy niemieckiego na pewno się przydadzą. Warto pamiętać, że w sobotę może być trudniej o podwózkę,
                        gdyż wielu Żydów obchodzi Sabat i w ten dzień nie wsiadają do samochodu. O wiele łatwiej złapać stopa w niedzielę, która
                        jest dniem pracującym. Lepiej unikać rozmów o sytuacji politycznej kraju, chyba że kierowca sam na to nalega.</p>
